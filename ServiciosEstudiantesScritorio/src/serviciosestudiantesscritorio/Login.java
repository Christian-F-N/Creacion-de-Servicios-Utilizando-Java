    private static String login(java.lang.String idUser, java.lang.String passUser) {
        ws.Servicio_Service service = new ws.Servicio_Service();
        ws.Servicio port = service.getServicioPort();
        return port.login(idUser, passUser);
    }

    private static java.util.List<ws.Esrudiantes> obtEstudiante(java.lang.String arg0) {
        ws.Servicio_Service service = new ws.Servicio_Service();
        ws.Servicio port = service.getServicioPort();
        return port.obtEstudiante(arg0);
    }

    private static java.util.List<ws.Profesor> filtrarProfe(java.lang.String arg0) {
        ws.Servicio_Service service = new ws.Servicio_Service();
        ws.Servicio port = service.getServicioPort();
        return port.filtrarProfe(arg0);
    }

