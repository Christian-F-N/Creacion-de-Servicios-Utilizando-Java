    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        String usuario = this.user.getText();
        String passw = this.clave.getText();
        String pro = "PROFESOR";
        String est = "ESTUDIANTE";
        String a = login(usuario, passw);
        if (a.equals(pro) || a.equals(est)) {

            if (a.equals(pro)) {
                List<Profesor> c = filtrarProfe(usuario);
                String b = (String.valueOf(c.get(0).getNOMBRE()));
                JOptionPane.showMessageDialog(null, "BIENVENIDO " + b);
                Profeshor d = new Profeshor();
                d.setVisible(true);
                this.dispose();
                Profeshor.txtuser.setText(user.getText());
            } else {
                List<Esrudiantes> c = obtEstudiante(usuario);
                String b = (String.valueOf(c.get(0).getNOMBRE()));
                JOptionPane.showMessageDialog(null, "BIENVENIDO " + b);
                Estudiante e = new Estudiante();
                e.setVisible(true);
                this.dispose();
                Estudiante.txtuser.setText(user.getText());
                
            }

        } else {
            JOptionPane.showMessageDialog(null, "Datos incorrectos");
        }
    }//GEN-LAST:event_ingresarActionPerformed
