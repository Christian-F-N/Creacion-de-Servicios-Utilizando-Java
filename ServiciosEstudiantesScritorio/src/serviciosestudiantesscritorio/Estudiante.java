 private void llenarN(List<Notas> p) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID NOTA");
        modelo.addColumn("ID PARCIAL");
        modelo.addColumn("ID DOCENTE");
        modelo.addColumn("ID ESTUDIANTE");
        modelo.addColumn("NOTA");
        for (int i = 0; i < p.size(); i++) {
            Object[] obj = new Object[5];
            obj[0] = (String.valueOf(p.get(i).getIDNOTA()));
            obj[1] = (String.valueOf(p.get(i).getIDPARCIAL()));
            obj[2] = (String.valueOf(p.get(i).getIDDOCENTE()));
            obj[3] = (String.valueOf(p.get(i).getIDESTUD()));
            obj[4] = (String.valueOf(p.get(i).getNOTA()));
            modelo.addRow(obj);
            tabla.setModel(modelo);

        }
    }

    private void llenarESt(List<Esrudiantes> a) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID ESTUDIANTE");
        modelo.addColumn("ID CUENTA");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("PROMEDIO P1");
        modelo.addColumn("PROMEDIO P2");
        for (int i = 0; i < a.size(); i++) {
            Object[] obj = new Object[6];
            obj[0] = (String.valueOf(a.get(i).getIDESTUD()));
            obj[1] = (String.valueOf(a.get(i).getIDCUENTA()));
            obj[2] = (String.valueOf(a.get(i).getNOMBRE()));
            obj[3] = (String.valueOf(a.get(i).getAPELLIDO()));
            obj[4] = (Double.valueOf(a.get(i).getPROMEDIO1()));
            obj[5] = (Double.valueOf(a.get(i).getPROMEDIO2()));
            modelo.addRow(obj);
            tabla.setModel(modelo);

        }
    }
