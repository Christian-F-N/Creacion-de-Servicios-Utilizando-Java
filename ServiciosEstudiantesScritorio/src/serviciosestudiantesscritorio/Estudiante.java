    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        this.p1.setText(String.valueOf(parcialUno()));
        this.p2.setText(String.valueOf(parcialDos()));
        Double p1 = parcialUno();
        Double p2 = parcialDos();
        Double prom = p1 + p2;
        this.pro.setText(String.valueOf(prom));
        if (prom < 10.99) {
            this.status.setText("REPRUEBA");
        }
        if (prom < 13.99 && prom > 11.00) {
            this.status.setText("SUPLETORIO");
        }
        if (prom < 20.00 && prom > 14.00) {
            this.status.setText("APROBADO");
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    public Double parcialUno() {
        Double aux = 0.0;
        String usuario = txtuser.getText();
        List<Esrudiantes> c = obtEstudiante(usuario);
        String b = (String.valueOf(c.get(0).getIDESTUD()));
        List<Notas> p1 = filtroNotas("P1", b);
        for (int i = 0; i < p1.size(); i++) {
            Double a = (Double.valueOf(p1.get(i).getNOTA()));
            aux += a;
        }
        int total = p1.size();
        Double prom = aux / Double.valueOf(total);
        return prom;
    }

    public Double parcialDos() {
        Double aux = 0.0;
        String usuario = txtuser.getText();
        List<Esrudiantes> c = obtEstudiante(usuario);
        String b = (String.valueOf(c.get(0).getIDESTUD()));
        List<Notas> p1 = filtroNotas("P2", b);
        for (int i = 0; i < p1.size(); i++) {
            Double a = (Double.valueOf(p1.get(i).getNOTA()));
            aux += a;
        }
        int total = p1.size();
        Double prom = aux / Double.valueOf(total);
        return prom;

    }

